package com.recruitmentportal.DTO;

public class ExclusiveReports {
	
	
	private String client;
	private Integer requirements;
	private Integer positions;
	private Integer profiles;
	private Integer toClient;
	public String getClient() {
		return client;
	}
	public void setClient(String client) {
		this.client = client;
	}
	public Integer getRequirements() {
		return requirements;
	}
	public void setRequirements(Integer requirements) {
		this.requirements = requirements;
	}
	public Integer getPositions() {
		return positions;
	}
	public void setPositions(Integer positions) {
		this.positions = positions;
	}
	public Integer getProfiles() {
		return profiles;
	}
	public void setProfiles(Integer profiles) {
		this.profiles = profiles;
	}
	public Integer getToClient() {
		return toClient;
	}
	public void setToClient(Integer toClient) {
		this.toClient = toClient;
	}
	

}
